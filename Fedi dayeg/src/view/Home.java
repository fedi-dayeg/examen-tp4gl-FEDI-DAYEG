    public void actualiser(){
        ArrayList<Auteur> ALF = new AuteurController().afficher();
        if(ALF != null) {
            String col[] = {"Id","sociale","nom","prenom","date_dec","date_dec"};
            DefaultTableModel tableModel = new DefaultTableModel(col,0);
            for (Auteur f: ALF){
                Object row[] = new Object[]{f.getORCID(),f.getNumSecurite(),f.getNom(),f.getPrenom(),f.getDate_dec(),f.getDate_naiss()};
                tableModel.addRow(row);
            }
            jTable1.setModel(tableModel);
        }
        else {
            JOptionPane.showMessageDialog(this,"aucun donnée","Errur",JOptionPane.ERROR_MESSAGE);
        }
    }
